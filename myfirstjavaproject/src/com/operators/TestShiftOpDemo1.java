package com.operators;

public class TestShiftOpDemo1 {

	public static void main(String[] args) {

		int a = 69;
		int b = 2;

		System.out.println(a << b);
		System.out.println(a >> b);//
		System.out.println(a >>> b);

		int x = 56;
		int y = 3;
		System.out.println(x << y);
		System.out.println(x >> y);//7

	}

}
